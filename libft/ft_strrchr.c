#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*p;
	int		i;

	p = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (((char *)s)[i] == (char)c)
		{
			p = &((char *)s)[i];
		}
		i++;
	}
	if ((char)c == '\0')
		p = &((char *) s)[i];
	return (p);
}
